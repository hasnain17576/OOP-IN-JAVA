class person {
    // attributes (data members)
    String name;   // predefined data type (String)
    int age;       // predefined data type (int)
    double height; // predefined data type (double)

    // method (behavior)
    void displayInfo() {
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
    }
}
