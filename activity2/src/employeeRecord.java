public class employeeRecord {
    private int emp_id;
    private double salary;
     public employeeRecord(){

     }
     public void setEmp_id(int id ){
        emp_id=id;
     }
     public int getEmp_id(){
        return emp_id;

     }
     public void setSalary (int sal){
        salary=sal;
     }
     public double getSalary(){
        return salary;
     }
}
